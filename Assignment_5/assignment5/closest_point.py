from scipy.spatial import KDTree
import numpy as np

class ClosestPoint:
    def __init__(self):
        self.kdtree = None

    def init(self, pts):
        self.kdtree = KDTree(pts)

    def get_closest_point(self, query):
        dist, idx = self.kdtree.query(query)
        return idx